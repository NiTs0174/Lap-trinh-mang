    private void txtReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtReadActionPerformed
        // TODO add your handling code here:
        try{
            FileReader file = new FileReader(txtPath.getText());
            BufferedReader buffer = new BufferedReader(file);
            
            List<PhanSo> phanSoCrudes = new ArrayList<PhanSo>();
            String line;
            String[] lineSplit;
            while ((line=buffer.readLine()) != null) {
                System.out.println(line);
                lineSplit = line.split(" / ");
                phanSoCrudes.add(new PhanSo(Integer.parseInt(lineSplit[0]),Integer.parseInt(lineSplit[1])));
            }
            file.close();
            
            List<PhanSo> phanSoPrimes = new ArrayList<PhanSo>();
            List<PhanSo> phanSoNotPrimes = new ArrayList<PhanSo>();
            for(int i =0;i<phanSoCrudes.size();i++){
                if(isPrimeNumber(phanSoCrudes.get(i).mauSo)){
                    phanSoPrimes.add(phanSoCrudes.get(i));
                }else{
                    phanSoNotPrimes.add(phanSoCrudes.get(i));
                }
            }
            
            DefaultListModel viewList=new DefaultListModel();
            for(int i=0;i<phanSoPrimes.size();i++){
                viewList.addElement(phanSoPrimes.get(i).toString());
            }
            listNT.setModel(viewList);
            
            viewList=new DefaultListModel();
            for(int i=0;i<phanSoNotPrimes.size();i++){
                viewList.addElement(phanSoNotPrimes.get(i).toString());
            }
            listNotNT.setModel(viewList);
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtReadActionPerformed

