    public class PhanSo{
        private static final int MIN = 1;
        private static final int MAX = 1000;
        
        public int tuSo;
        public int mauSo;
        public PhanSo(int tuSo, int mauSo){
            this.tuSo = tuSo;
            this.mauSo = mauSo;
        }
        public PhanSo(){
            Random rand = new Random();
            int tuSoRandom = rand.nextInt((MAX - MIN) + 1) + MIN;
            int mauSoRandom = rand.nextInt((MAX - MIN) + 1) + MIN;
//            int tuSoRandom = ThreadLocalRandom.current().nextInt(MIN, MAX + 1);
//            int mauSoRandom = ThreadLocalRandom.current().nextInt(MIN, MAX + 1);
            this.tuSo = tuSoRandom;
            this.mauSo = mauSoRandom;
        }
        public String toString(){
            return this.tuSo+" / "+this.mauSo;
        }
    }
    public static boolean isPrimeNumber(int n) {
        // so nguyen n < 2 khong phai la so nguyen to
        if (n < 2) {
            return false;
        }
        // check so nguyen to khi n >= 2
        int squareRoot = (int) Math.sqrt(n);
        for (int i = 2; i <= squareRoot; i++) {
            if (n % i == 0) {
                return false;
            }
        }
        return true;
    }
